// Helper functions
export function getVehicleById(id: string): Vehicle | undefined {
  return mockVehicles.find(v => v.id === id)
}

export function getServiceHistoryByVehicleId(vehicleId: string): ServiceHistoryEntry[] {
  return mockServiceHistory
    .filter(s => s.vehicleId === vehicleId)
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
}

export function getMaintenanceAlertsByVehicleId(vehicleId: string): MaintenanceAlert[] {
  return mockMaintenanceAlerts
    .filter(a => a.vehicleId === vehicleId)
    .sort((a, b) => {
      const priorityOrder = { critical: 0, high: 1, medium: 2, low: 3 }
      return priorityOrder[a.priority] - priorityOrder[b.priority]
    })
}

export function getDocumentsByVehicleId(vehicleId: string): VehicleDocument[] {
  return mockDocuments.filter(d => d.vehicleId === vehicleId)
}

